    public void LoadData() {
        lst = manager.getAllReader();

        for (Reader rea : lst) {
            Vector v = new Vector();
            v.add(rea.getId());
            v.add(rea.getName());
            v.add(rea.getEmail());
            v.add(rea.getPhone());
            v.add(rea.getAddress());
            v.add(rea.isStatus() ? "Show" : "Hidden");

            model.addRow(v);
        }
        tblReader.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int pos = tblReader.getSelectedRow();
                if (pos < 0) {
                    pos = 0;
                }
                reader = lst.get(pos);
                SetData(reader);
            }
        });
        tblReader.setModel(model);
    }

    public void SearchData(String Name) {
        model = (DefaultTableModel) tblReader.getModel();
        model.setRowCount(0);

        lst = manager.getOneReader(Name);

        for (Reader rea : lst) {
            Vector v = new Vector();
            v.add(rea.getId());
            v.add(rea.getName());
            v.add(rea.getEmail());
            v.add(rea.getPhone());
            v.add(rea.getAddress());
            v.add(rea.isStatus() ? "Show" : "Hidden");

            model.addRow(v);
        }
        tblReader.setModel(model);
    }

    public void SetData(Reader rea) {
        txtName.setText(rea.getName());
        txtEmail.setText(rea.getEmail());
        txtPhone.setText(rea.getPhone());
        txtAddress.setText(rea.getAddress());
        if (rea.isStatus() == true) {
            cboStatus.setSelectedItem("Show");
        } else {
            cboStatus.setSelectedItem("Hidden");
        }
    }


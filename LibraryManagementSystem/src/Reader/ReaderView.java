    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        // TODO add your handling code here:
        String name = txtName.getText();
        String email = txtEmail.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Name");
            return;
        } else if (name.length() >= 64) {
            JOptionPane.showMessageDialog(this, "Name Length Must Be Less Than 64 Characters");
            return;
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Email");
            return;
        } else if (isValidEmail(email) == false) {
            JOptionPane.showMessageDialog(this, "Wrong Email Format (abc123@gmail.com)");
            return;
        } else if (phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Phone");
            return;
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address");
            return;
        } else if (address.length() >= 64) {
            JOptionPane.showMessageDialog(this, "Address Length Greater Must Be Less Than 200 Characters");
            return;
        } else if (cboStatus.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Must Choose A Status");
            return;
        }
        String sta = cboStatus.getSelectedItem().toString();

        boolean Status;
        if (sta.equals("Show")) {
            Status = true;
        } else {
            Status = false;
        }

        Reader r = new Reader(name, email, phone, address, Status);
        int row = manager.CreateReader(r);
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Create Reader Success !");
        } else {
            JOptionPane.showMessageDialog(this, "Create Reader Error !", "Inane error", JOptionPane.ERROR_MESSAGE);
        }
        model = (DefaultTableModel) tblReader.getModel();
        model.setRowCount(0);

        LoadData();

    }//GEN-LAST:event_btnAddActionPerformed

